// 1. ++ -> increase by 1

// a. postfix
let a = 10
console.log("Result:",a++)
console.log("Result 1:",a)



// b. prefix
let x = 11
console.log("Result 2:", ++x)
console.log("Result 3:", x)



// 2. -- -> decrease by 1

// a. postfix
let b = 10
console.log("Result:",b--)  // 10
console.log("Result 1:",b)  // 9



// b. prefix
let c = 11
console.log("Result 2:", --c)  // 10
console.log("Result 3:", c)  // 10
